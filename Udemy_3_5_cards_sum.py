#дан список карт, у каждой есть свой вес. 2, 3 , 4, 5 6 весят +1
# 7, 8 и 9 весят 0
# 10, J, Q, K, A весят -1
# имея список карт посчитать их общий вес
current_hand = [2, 3, 4, 10, 'Q', 5]

dict_cards = {
    2: 1, 
    3: 1, 
    4: 1,
    5: 1, 
    6: 1,
    7: 0,
    8: 0,
    9: 0,
    10: -1,
    "J": -1,
    "Q": -1,
    "K": -1,
    "A": -1,
} 

total = sum([dict_cards[x] for x in current_hand])

print(total)
